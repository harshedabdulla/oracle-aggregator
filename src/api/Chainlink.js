// src/api/Chainlink.js
import { ethers } from 'ethers';

const provider = new ethers.providers.JsonRpcProvider("https://rpc.ankr.com/eth_sepolia");
const CHAINLINK_BTC_USD_ADDRESS = "0x1b44F3514812d835EB1BDB0acB33d3fA3351Ee43";

const aggregatorV3InterfaceABI = [
  {
    inputs: [],
    name: "decimals",
    outputs: [{ internalType: "uint8", name: "", type: "uint8" }],
    stateMutability: "view",
    type: "function",
  },
  {
    inputs: [],
    name: "description",
    outputs: [{ internalType: "string", name: "", type: "string" }],
    stateMutability: "view",
    type: "function",
  },
  {
    inputs: [{ internalType: "uint80", name: "_roundId", type: "uint80" }],
    name: "getRoundData",
    outputs: [
      { internalType: "uint80", name: "roundId", type: "uint80" },
      { internalType: "int256", name: "answer", type: "int256" },
      { internalType: "uint256", name: "startedAt", type: "uint256" },
      { internalType: "uint256", name: "updatedAt", type: "uint256" },
      { internalType: "uint80", name: "answeredInRound", type: "uint80" },
    ],
    stateMutability: "view",
    type: "function",
  },
  {
    inputs: [],
    name: "latestRoundData",
    outputs: [
      { internalType: "uint80", name: "roundId", type: "uint80" },
      { internalType: "int256", name: "answer", type: "int256" },
      { internalType: "uint256", name: "startedAt", type: "uint256" },
      { internalType: "uint256", name: "updatedAt", type: "uint256" },
      { internalType: "uint80", name: "answeredInRound", type: "uint80" },
    ],
    stateMutability: "view",
    type: "function",
  },
  {
    inputs: [],
    name: "version",
    outputs: [{ internalType: "uint256", name: "", type: "uint256" }],
    stateMutability: "view",
    type: "function",
  },
];

export const fetchChainlinkPrice = async () => {
  try {
    const priceFeed = new ethers.Contract(CHAINLINK_BTC_USD_ADDRESS, aggregatorV3InterfaceABI, provider);
    const roundData = await priceFeed.latestRoundData();
    console.log("Latest Round Data", roundData);
    return roundData;
  } catch (error) {
    console.error('Error fetching Chainlink price:', error);
    return null;
  }
};
